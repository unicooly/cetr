<ch1>到南京时，有朋友约去游逛，勾留了一日；第二日上午便须渡江到浦口，下午上车北去。


<ch2>我们过了江，进了车站。

<ch3>我看见他戴着黑布小帽，穿着黑布大马褂，深青布棉袍，蹒跚地走到铁道边，慢慢探身下去，尚不大难。


<ch4>这时我看见他的背影，我的泪很快地流下来了。



<ch5>过铁道时，他先将桔子散放在地上，自己慢慢爬下，再抱起橘子走。


<ch6>近几年来，父亲和我都是东奔西走，家中光景是一日不如一日。


<ch7>我北来后，他写了一信给我，信中说道：“我身体平安，惟膀子疼痛厉害，举箸提笔，诸多不便，大约大去之期不远矣。”
